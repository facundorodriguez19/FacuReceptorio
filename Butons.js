

document.getElementById("boton1").addEventListener("click", function (e) {

    e.preventDefault();

    let main = document.getElementById("main");

    main.classList.add('displaynone');

    let categorias = document.getElementById("buscador_productos");

    categorias.classList.remove('displaynone');
    categorias.classList.add('displayblock');

    let cuadradoAzul = document.getElementById("categorias_principales");

    cuadradoAzul.classList.remove('cuadrado_azul')

})

